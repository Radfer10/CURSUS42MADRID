#include "printf.h"

void	ft_putstr_pf(char *str, int *len)
{
	int	i;

	i = 0;
	if (!str)
			str = "(null)";
		while (str[i] != '\0')
		{
			write(1, &str[i], 1);
		i++;
		(*len)++;
		}
}
